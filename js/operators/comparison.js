// Comparison Operators


// 1. > (Greater than)
// 2. < (Less than)
// 3. == (Double is eqauals to)
// 4. >= (Greater than or is equal to)
// 5. <= (Less than or is equal to)

var a = 10
var b = 20

// 1. > (Greater than)

if (b > a) {
    console.log("B is greater")
} else {
    console.log("B is smaller")
}

// 2. < (Less than)

if (b < a) {
    console.log("B is smaller")
} else {
    console.log("B is greater")
}


// 3. == (Double is equals to)

if (a == b) {
    console.log("Same")
} else {
    console.log("Different")
}

// 4. >= (Greater than or is equal to)

if (a >= b) {
    console.log("A is greater")
} else {
    console.log("B is greater")
}

// 5. <= (Less than or is equal to)

if (a <= b) {
    console.log("A is smaller")
} else {
    console.log("A is greater")
}
